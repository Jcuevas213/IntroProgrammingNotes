# Assignment Operator (equal sign)
x = 5  # the variable x has been assigned a value of 5
print("x =", x)
x = 6  # value is overwritten
print("x =", x)
x = x + 1  # can't do this in math
print("x =", x)
x + 2  # no effect
print("x =", x)
# x + 1 = y  # works in math, not in programming

# coding shortcut
x += 1  # add to yourself
print("x =", x)
x += 10
print("x =", x)

